import { toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

/** Estilização com atributos ja definidos */
export const showToast = (mensagem) => {
  toast(mensagem, {
    position: "top-right",
    autoClose: 3000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
  });
};

